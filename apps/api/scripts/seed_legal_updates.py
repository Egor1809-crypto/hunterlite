async def seed() -> int:
    """Insert seed legal updates. Returns count of new records."""
    count = 0
    async with async_session() as db:
        for item in SEED_UPDATES:
            existing = await db.execute(
                select(LegalUpdate).where(LegalUpdate.title == item["title"])
            )
            if existing.scalar_one_or_none() is not None:
                continue

            published_at = datetime.strptime(
                item["published_at"], "%Y-%m-%d"
            ).replace(tzinfo=timezone.utc)

            record = LegalUpdate(
                id=uuid.uuid4(),
                title=item["title"],
                summary=item["summary"],
                source=item["source"],
                category=item["category"],
                relevance_score=item["relevance_score"],
                published_at=published_at,
                fetched_at=datetime.now(timezone.utc),
                is_ai_generated=False,
                tags=item["tags"],
                is_active=True,
            )
            db.add(record)
            count += 1

        if count > 0:
            await db.commit()
    return count


async def main():
    count = await seed()
    print(f"Seeded {count} legal updates ({len(SEED_UPDATES)} total defined, {len(SEED_UPDATES) - count} already existed)")


if __name__ == "__main__":
    asyncio.run(main())
