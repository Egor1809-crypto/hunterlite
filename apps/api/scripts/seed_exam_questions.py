async def seed() -> None:
    async with async_session() as session:
        existing = (await session.execute(
            select(ExamDefinition.id)
        )).scalars().all()

        if existing:
            print(f"Found {len(existing)} existing exam definitions, clearing old data...")
            await session.execute(text("DELETE FROM exam_certificates"))
            await session.execute(text("DELETE FROM exam_attempts"))
            await session.execute(text("DELETE FROM exam_questions"))
            await session.execute(text("DELETE FROM exam_definitions"))
            await session.commit()

        for exam_def in EXAM_DEFINITIONS:
            session.add(ExamDefinition(**exam_def))
        await session.flush()

        for q in QUESTIONS:
            session.add(ExamQuestion(
                id=uuid.uuid4(),
                exam_id=None,
                category=q["category"],
                difficulty=q["difficulty"],
                question_text=q["question_text"],
                options=q["options"],
                correct_option_id=q["correct_option_id"],
                explanation=q["explanation"],
                article_reference=q.get("article_reference"),
                is_active=True,
            ))

        await session.commit()
        print(f"Seeded {len(EXAM_DEFINITIONS)} exam definitions and {len(QUESTIONS)} questions.")


if __name__ == "__main__":
    asyncio.run(seed())
