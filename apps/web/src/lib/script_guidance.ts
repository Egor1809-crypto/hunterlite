/** Quick lookup by stage number 1..7. Returns null for invalid input. */
export function guidanceFor(stageNumber: number | null | undefined): StageGuidance | null {
  if (!stageNumber || stageNumber < 1 || stageNumber > STAGE_GUIDANCE.length) {
    return null;
  }
  return STAGE_GUIDANCE[stageNumber - 1] ?? null;
}
